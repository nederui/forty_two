#include "includes/libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t buffer)
{
	size_t	i;

	i = 0;
	if (buffer > 0)
	{
		while (src[i] && i < (buffer - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (ft_strlen(src));
}
