#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	nbr;
	size_t	sign;

	nbr = 0;
	sign = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == 45)
			sign = sign * -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		nbr = (10 * nbr) + (str[i] - 48);
		i++;
	}
	return (sign * (int)nbr);
}
