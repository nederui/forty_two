#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb = -2147483648)
	{
		write(1, '-', 1);
		write(1, "2", 1);
		ft_putnbr(147483648);
	}
	if (nb < 0)
	{
		nb = -nb;
		ft_putnbr(nb);
	}
	if (nb > 9)
	{
		ft_putnbr(nb/10 + 48);
		ft_putnbr(nb%10 + 48);
	}
	else
	{
		nb + 48;
	}
}